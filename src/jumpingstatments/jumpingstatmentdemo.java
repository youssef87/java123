package jumpingstatments;

public class jumpingstatmentdemo {

	public static void main(String[] args) {
	   // jumping statement
		//data type=int , condition=
		// if the programme  reach 7 break it which bring us a numbers from 1 to 6
		// instead of break put continue.
		for(int a=1; a<=10; a++)
		{
			if(a==7)
			{
				break;
			}
		System.out.println(a);
	}
		
		
		
		
		

	}

}
